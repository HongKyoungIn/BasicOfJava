package practice;

public class Q01 {

    public static void main(String[] args) {
        int x = 10;
        char ch = ' ';
        char ch2 = 'B';
        int year = 0;
        boolean powerOn = true;
        String str = "yes";

        if(x<10 && x <20);
        if(!(ch==' ' || ch =='\t'));
        if(ch=='x' || ch=='X');
        if('0'<=ch && ch<='9');
        if(('a' <= ch2 && ch2 <= 'z') || ('A' <= ch2 && ch2 <= 'Z'));
        if(year%400 == 0 || (year%4 == 0 && year%100 != 0));
        if(powerOn == false);
        if(str.equals("yes"));
    }

}